#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long int	sign;
	long		res;

	i = 0;
	sign = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
		if (res * sign > 2147483647)
			return (-1);
		if (res * sign < -2147483648)
			return (0);
	}
	return (res * sign);
}
